public class Heroi {
    private int ataque;
    private int defesa;
    private int pontos;

    public Heroi() {}

    public Heroi (int ataque, int defesa, int pontos){
        this.ataque = ataque;
        this.defesa = defesa;
        this.pontos = pontos;
    }
    public int getAtaque(){
        return this.ataque;
    }
    public void setAtaque(){
        this.ataque = ataque;
    }
    public int getDefesa(){
        return  this.defesa;
    }
    public void setDefesa(){
        this.defesa = defesa;
    }
    public int getPontos(){
        return this.pontos = pontos;
    }
    public void setPontos(){
        this.pontos = pontos;
    }

}
